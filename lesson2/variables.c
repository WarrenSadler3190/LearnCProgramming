#include <stdio.h>
int main()
{
	int age = 5;
	printf("Age: %d\n", age);
	/* %d is the integer spaceholer */

	double price = 20.50;
	printf("The shirt cost %lf\n", price);
	/* %lf is the spaceholder for doubles */

	char first_initial = 'W';
	printf("My name starts with a %c\n", first_initial);

	/* char's have single qoutes */

	
}
